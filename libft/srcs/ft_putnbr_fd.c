#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		i;
	char	arr[10];

	i = 0;
	if (n == 0)
		write(fd, "0", 1);
	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	if (n < 0 && n != -2147483648)
	{
		write(fd, "-", 1);
		n = n * (-1);
	}
	while (n > 0)
	{
		arr[i++] = n % 10 + '0';
		n /= 10;
	}
	while (i > 0)
		write(fd, &arr[--i], 1);
}
